class JLab0302{
	public static void main(String[] args){
		float a = 7.0f;
		float b = 3.0f;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a * b);
		System.out.println(a/b);
		System.out.println(a % b);
		System.out.println(a++ +b);
		System.out.println(++a+b);
		System.out.println(a--*b);
		System.out.println(--a*b);
	}
}